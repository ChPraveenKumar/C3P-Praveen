package com.techm.c3p.core.pojo;

public class SiteInfoPojo {
	private String custName;
	private String siteName;
	private String siteRegion;
	private String siteActive;

	public String getCustName() {
		return custName;
	}

	public void setCustName(String custName) {
		this.custName = custName;
	}

	public String getSiteName() {
		return siteName;
	}

	public void setSiteName(String siteName) {
		this.siteName = siteName;
	}

	public String getSiteRegion() {
		return siteRegion;
	}

	public void setSiteRegion(String siteRegion) {
		this.siteRegion = siteRegion;
	}

	public String getSiteActive() {
		return siteActive;
	}

	public void setSiteActive(String siteActive) {
		this.siteActive = siteActive;
	}

	

}

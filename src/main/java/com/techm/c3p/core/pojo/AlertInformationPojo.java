package com.techm.c3p.core.pojo;

public class AlertInformationPojo {
	
	private String alert_code;
	private String alert_description;
	private String alert_category;
	private String alert_type;
	public String getAlert_type() {
		return alert_type;
	}
	public void setAlert_type(String alert_type) {
		this.alert_type = alert_type;
	}
	public String getAlert_code() {
		return alert_code;
	}
	public void setAlert_code(String alert_code) {
		this.alert_code = alert_code;
	}
	public String getAlert_description() {
		return alert_description;
	}
	public void setAlert_description(String alert_description) {
		this.alert_description = alert_description;
	}
	public String getAlert_category() {
		return alert_category;
	}
	public void setAlert_category(String alert_category) {
		this.alert_category = alert_category;
	}

}
